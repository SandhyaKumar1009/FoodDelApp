/**
 * Get image URL for a food item
 * @param {string} dishName - Name of the dish
 * @returns {string} Image URL
 */
export const getFoodImage = (dishName) => {
  if (!dishName) return foodImageMap.default;
  
  // Try exact match first
  if (foodImageMap[dishName]) {
    return foodImageMap[dishName];
  }
  
  // Try partial match (e.g., "Chicken" in "Chicken Biryani")
  const lowerName = dishName.toLowerCase();
  for (const [key, url] of Object.entries(foodImageMap)) {
    if (key.toLowerCase() === lowerName) {
      return url;
    }
  }
  
  // Try keyword-based matching
  const keywords = ['chicken', 'biryani', 'dosa', 'butter', 'paneer', 'pizza', 'burger', 'fish', 'samosa'];
  for (const keyword of keywords) {
    if (lowerName.includes(keyword) && foodImageMap[keyword]) {
      return foodImageMap[keyword];
    }
  }
  
  return foodImageMap.default;
};

export default foodImageMap;
